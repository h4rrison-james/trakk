    public static function getTimezonesWithLocalTime() {
        $validTimezones = self::getTimezones();
        $times = array();
        $date  = Zend_Date::now();
        
        foreach ($validTimezones as $timezone => $name) {
            try {
                //Work out the time in this timezone
                $date->setTimezone($timezone);
                
                $times[] = array(
                    'timezone'  => $timezone,
                    'name'      => $name,
                    'localTime' => $date->toString('EEE d MMM HH:mm a'),
                );
            } catch (Exception $e) {
                /**
                 * There must be a problem with this timezone on this server.
                 * We will ignore this timezone for now.
                 */
            }
        }
        
        return $times;
    }
    
    public static function isValidTimezone($timezone) {
        $validTimezones = self::getTimezones();
        
        return array_key_exists($timezone, $validTimezones);
    }
    
    public static function setUserTimezone($timezone) {
        if (strlen($timezone) > 0) {
            if (self::isValidTimezone($timezone)) {
                Zend_Registry::set('userTimezone', $timezone);
            } else {
                throw new Bugify_Exception('Invalid timezone.');
            }
        }
    }
    
    public static function getUserTimezone() {
        if (Zend_Registry::isRegistered('userTimezone')) {
            $timezone = Zend_Registry::get('userTimezone');
        } else {
            $timezone = self::DEFAULT_TIMEZONE;
        }
        
        return $timezone;
    }
    
    public static function getLocalTime($gmtTimestamp) {
        $gmtTimestamp = ((int)$gmtTimestamp > 0) ? $gmtTimestamp : 0;
        
        //Convert to users' timezone
        $date = new Zend_Date($gmtTimestamp, Zend_Date::TIMESTAMP);
        $date->setTimezone(self::getUserTimezone());
        
        return $date->getIso();
    }
    
    public static function getTimestamp($dateString) {
        if (strlen($dateString) > 0) {
            $date = new Zend_Date();
            $date->setTimezone(self::getUserTimezone());
            $date->set($dateString, Zend_Date::ISO_8601);
            
            $timestamp = $date->getTimestamp();
        } else {
            $timestamp = 0;
        }
        
        return $timestamp;
    }
    
    public static function formatDate($format, $dateString) {
        if (!Zend_Date::isDate($dateString, Zend_Date::ISO_8601)) {
            //Make up a date
            $dateString = '1970-01-01T00:00:00+00:00';
        }
        
        //Load the date
        $date = new Zend_Date();
        $date->setTimezone(self::getUserTimezone());
        $date->set($dateString, Zend_Date::ISO_8601);
        
        return $date->toString($format);
    }
